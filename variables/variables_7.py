NAME = 'Victor'
print('Good Morning, ' + NAME)
print('Good Afternoon, ' + NAME)
print('Good Evening, ' + NAME)

NAME = 'Nina'
print('Good Morning, ' + NAME)
print('Good Afternoon, ' + NAME)
print('Good Evening, ' + NAME)

#It'll print Good Morning, Nina, then Good Afternoon, Nina, and Good Evening Nina.
# this is because NAME was re-assigned, and Python has no real immutable constants. 
